package Assignment2;

public class SwapingfTwoNumbers {
public static void main(String[] args) {
	System.out.println("beforing swaping");
	int a = 20;
	int b = 30;
	int c;
	System.out.println("a =" + a);
	System.out.println("b =" + b);
	System.out.println("After Swaping");
	c = a;
	a = b;
	b = c;
	System.out.println("a =" + a);
	System.out.println("b =" + b);
}
}
